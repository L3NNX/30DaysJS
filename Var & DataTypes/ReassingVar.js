let name = "John Doe";
console.log(name); 


name = "Jane Smith";
console.log(name); // Output: "Jane Smith"



//The key difference between let and const is that const variables are meant to be constant and cannot be reassigned. Once a const variable is declared and assigned a value, that value cannot be changed.
const names = "John Doe";
console.log(names); 

names = "Jane Smith";
console.log(names);