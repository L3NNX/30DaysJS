
const twoDimensionalArray = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];

console.log("Two-Dimensional Array:", twoDimensionalArray);


// Accessing a specific element
var rowIndex = 1; 
var colIndex = 2; 
var specificElement = twoDimensionalArray[rowIndex][colIndex];

// Log the specific element to the console
console.log("Element at [" + rowIndex + "][" + colIndex + "]: " + specificElement); 